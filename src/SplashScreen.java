private void fadeIn() {

    setOpacity(0f);   // start fully transparent

    Timer fadeTimer = new Timer(20, null);

    fadeTimer.addActionListener(new java.awt.event.ActionListener() {

        float opacity = 0f;

        @Override
        public void actionPerformed(java.awt.event.ActionEvent e) {

            opacity += 0.05f;   // increase speed here

            if (opacity >= 1f) {
                opacity = 1f;
                fadeTimer.stop();
            }

            setOpacity(opacity);
        }
    });

    fadeTimer.start();
}
