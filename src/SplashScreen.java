    public SplashScreen() {

    setUndecorated(true);

    setSize(500, 560);   // 500 for image + space for progress bar
    setLocationRelativeTo(null);

   getContentPane().setBackground(new Color(245,245,245));
    getContentPane().setLayout(new BorderLayout());

    // Load logo
    ImageIcon icon = new ImageIcon(
            getClass().getResource("logo.png")
    );

    JLabel logoLabel = new JLabel(icon);
    logoLabel.setHorizontalAlignment(SwingConstants.CENTER);
logoLabel.setBorder(
    BorderFactory.createEmptyBorder(10, 10, 10, 10)
);
    // Progress Bar
    progressBar = new JProgressBar();
    progressBar.setStringPainted(true);
    progressBar.setBorderPainted(false);
    progressBar.setPreferredSize(new Dimension(500, 25));
    progressBar.setForeground(new Color(70,130,180));

    add(logoLabel, BorderLayout.CENTER);
    add(progressBar, BorderLayout.SOUTH);
fadeIn();
    startLoading();
}
private void startLoading() {

    javax.swing.Timer timer = new javax.swing.Timer(30, null);

    timer.addActionListener(new java.awt.event.ActionListener() {

        int value = 0;

        public void actionPerformed(java.awt.event.ActionEvent evt) {

            value++;
            progressBar.setValue(value);

            if (value >= 100) {
                timer.stop();

new ServerSelection().setVisible(true);

                dispose();
            }
        }
    });

    timer.start();
}
