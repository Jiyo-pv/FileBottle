    private void btnRunServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunServerActionPerformed
        try {

        try {
    String jarDir = new File(
        FileServer.class
            .getProtectionDomain()
            .getCodeSource()
            .getLocation()
            .toURI()
    ).getParent();

    String serverFolder = jarDir + File.separator + "FileBottleServer";

    System.out.println("SERVER ROOT: " + serverFolder);

     server = new FileServer(serverFolder);
new Thread(server).start();
} catch (Exception e) {
    e.printStackTrace();
}

        JFrame serverFrame = new JFrame("FileBottle Server");
serverFrame.setSize(450, 300);
serverFrame.setLocationRelativeTo(null);
serverFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
serverFrame.getContentPane().setBackground(new Color(18, 22, 30));
serverFrame.setLayout(new BorderLayout());
BufferedImage img = new BufferedImage(32, 32, BufferedImage.TYPE_INT_ARGB);
Graphics2D g = img.createGraphics();

g.setColor(new Color(41, 121, 255));
g.fillRoundRect(0, 0, 32, 32, 8, 8);

g.setColor(Color.WHITE);
g.setFont(new Font("Segoe UI", Font.BOLD, 30));
g.drawString("F", 8, 24);

g.dispose();

serverFrame.setIconImage(img);
JPanel mainPanel = new JPanel();
mainPanel.setBackground(new Color(18, 22, 30));
mainPanel.setLayout(new BoxLayout(mainPanel, BoxLayout.Y_AXIS));
mainPanel.setBorder(BorderFactory.createEmptyBorder(30, 30, 30, 30));

JLabel title = new JLabel("SERVER RUNNING");
title.setFont(new Font("Segoe UI", Font.BOLD, 22));
title.setForeground(new Color(41,121,255));
title.setAlignmentX(Component.CENTER_ALIGNMENT);

String ip = java.net.InetAddress.getLocalHost().getHostAddress();

JLabel ipLabel = new JLabel("IP: " + ip);
ipLabel.setFont(new Font("Segoe UI", Font.BOLD, 18));
ipLabel.setForeground(new Color(0, 200, 150));
ipLabel.setAlignmentX(Component.CENTER_ALIGNMENT);

// Connected Clients Label
JLabel clientsLabel = new JLabel("Connected Clients: 0");
clientsLabel.setFont(new Font("Segoe UI", Font.PLAIN, 16));
clientsLabel.setForeground(Color.WHITE);
clientsLabel.setAlignmentX(Component.CENTER_ALIGNMENT);

// Stop Button
JButton stopBtn = new JButton("Stop Server");
stopBtn.setFocusPainted(false);
stopBtn.setBackground(new Color(220, 53, 69));
stopBtn.setForeground(Color.WHITE);
stopBtn.setFont(new Font("Segoe UI", Font.BOLD, 14));
stopBtn.setAlignmentX(Component.CENTER_ALIGNMENT);

stopBtn.addActionListener(e -> {
    server.stopServer();
    JOptionPane.showMessageDialog(serverFrame, "Server Stopped.");
    System.exit(0);
});

mainPanel.add(title);
mainPanel.add(Box.createVerticalStrut(15));
mainPanel.add(ipLabel);
mainPanel.add(Box.createVerticalStrut(20));
mainPanel.add(clientsLabel);
mainPanel.add(Box.createVerticalStrut(25));
mainPanel.add(stopBtn);

serverFrame.add(mainPanel, BorderLayout.CENTER);
serverFrame.setVisible(true);
dispose();

new javax.swing.Timer(1000, e -> {
    clientsLabel.setText(
        "Connected Clients: " + FileServer.getConnectedClients()
    );
}).start();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this,
                "Server failed: " + e.getMessage());
    }
    }//GEN-LAST:event_btnRunServerActionPerformed

