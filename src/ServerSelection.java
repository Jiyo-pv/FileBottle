    private void btnCustomIPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomIPActionPerformed
      javax.swing.JTextField txtIP = new javax.swing.JTextField();

    Object[] message = {
        "Enter Server IP:",
        txtIP
    };

    int option = javax.swing.JOptionPane.showConfirmDialog(
            this,
            message,
            "Custom Server",
            javax.swing.JOptionPane.OK_CANCEL_OPTION
    );

    if (option == javax.swing.JOptionPane.OK_OPTION) {

        String serverIP = txtIP.getText();

        if (serverIP.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "IP cannot be empty");
            return;
        }

        new LoginRegister(serverIP).setVisible(true);
        dispose();
    }  // TODO add your handling code here:
    }//GEN-LAST:event_btnCustomIPActionPerformed

